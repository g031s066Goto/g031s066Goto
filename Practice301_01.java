//Practice301_01クラス:変数の値を表示
public class Practice301_01 {
	//メインメソッド
	public static void main(String[] args) {
		int num1; 							//変数num1を定義
		int num2; 							//変数num2を定義

		num1 = 1; 							//num1に1を代入
		num2 = 2; 							//num2に2を代入
		System.out.println(num1); 			//num1を出力
		System.out.println(num2); 			//num2を出力
		System.out.println((num1 + num2));	//num1とnum2の和を出力
	}
}

